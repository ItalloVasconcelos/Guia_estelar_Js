// Operadores logicos (Logical operators)

// -> 2 valores booleanos, quando verificados, resultará em true ou false

let pao = true
let queijo = true

// AND -> &&

console.log(pao && queijo) // True, mas se queijo for false, da false


// OR -> ||

console.log(pao || queijo) // True, pois ou é um true ou é outro.

// NOT -> !

console.log(!pao)// Fica false, pois era true.